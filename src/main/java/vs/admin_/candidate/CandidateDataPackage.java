package vs.admin_.candidate;

public class CandidateDataPackage {

	private String text;
	private int id;

	CandidateDataPackage() {
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
